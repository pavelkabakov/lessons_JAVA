package book_shild_beginners.charter05;
// Продемонстрировать применение оператора цикла do-while
public class DoWhile {
    public static void main(String[] args) {
        int n = 10;
        do {
            System.out.println("Число такта " + n);
        } while (--n > 0);
    }
}
